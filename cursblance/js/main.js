function renderModules() {
  const container = document.getElementById("modules");
  const viewed = new Set();
  modules.forEach((mod, i) => {
    const btn = document.createElement("button");
    btn.className = "module-button";
    btn.innerText = mod.title;
    btn.onclick = () => {
      const content = document.getElementById("mod" + i);
      const isOpen = content.style.display === "block";
      document.querySelectorAll(".module-content").forEach(div => div.style.display = "none");
      content.style.display = isOpen ? "none" : "block";
      viewed.add(i);
      updateProgress(viewed);
    };

    const contentDiv = document.createElement("div");
    contentDiv.id = "mod" + i;
    contentDiv.className = "module-content";
    contentDiv.innerHTML = `<h2>${mod.title}</h2><div>${mod.content}</div><p class='contact'>📩 Связь с автором: <a href='https://t.me/blancex'>@blancex</a></p>`;

    container.appendChild(btn);
    container.appendChild(contentDiv);
  });
}

function updateProgress(viewed) {
  const percent = Math.round((viewed.size / modules.length) * 100);
  document.getElementById("progress").innerText = `Прогресс: ${percent}%`;
}
